package com.company;

public class BankAccount {
    String account;
    Double balance;

    void setAccount(String acc) {
        this.account = acc;
    }

    void setBalance(Double bal) {
        this.balance = bal;
    }

    String getAccount() {
        return this.account;
    }

    Double getBalance() {
        return this.balance;
    }

}
